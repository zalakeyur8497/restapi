    public function register(Request $request)
    {
        $validator=validator::make($request->all(),

        [
            'name'=>'required',
            'email'=>'required',
            'password'=>'required',
         
        ]);
        if($validator->fails()){
            return $this->sendError('validation Eror',$validator->errors());
        }
        $input=$request->all();
        $data=User::create($input);
        $success['token']=$data->createToken('MyApp')->accessToken;

        $success['name']=$data->name;
        return $this->sendResponse($success,'User Register success');
    }

